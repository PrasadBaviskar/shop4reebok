function showCatlogues(d){
   
    d.forEach(function(product)
    { 
        let div = document.createElement("div");
// let parent = document.getElementById("products");

let img = document.createElement("img");
img.src = product.img;

let name = document.createElement("p");
name.textContent= product.name;
name.style.height = "50px"

let color = document.createElement("p");
color.textContent = "Color :" + product.color;

console.log(color)

let size = document.createElement("p");
size.textContent = "Size :" + product.size;

let mrp = document.createElement("p");
mrp.textContent = "MRP:-" +product.mrp;
mrp.style.fontSize = "14px"
mrp.style.textDecoration = "line-through"

let price = document.createElement("p");
price.textContent = "Discount Price: "+product.selling_price;

let btn = document.createElement("button");
btn.id = "btn_addto_cart"
btn.innerHTML = "Add to Cart"
btn.onclick = function(){
    add_to_cart(product);
}
img.onclick = function(){
    des(product);
}
div.append(img, name, color, size, price, mrp, btn);

parent.append(div);
});
}
